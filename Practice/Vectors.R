#
# Creating vectors in R
#

# Create a vector with numbers 1 to 40
vectorA <- c(1:40)

# Get the third element
vectorA[3]

# Return the sequence of elements from 5 to 15
vectorA[c(5:15)]

# Return the 29th element
vectorA[29]

# Create a vector of characters
charVector <- c("k", "j", "h", "a", "c", "m")

# Return the third element
charVector[3]

# Return the first, third and fifth element
charVector[c(1, 3, 5)]

# Return the sequence of characters from 1 to 4
charVector[c(1:4)]